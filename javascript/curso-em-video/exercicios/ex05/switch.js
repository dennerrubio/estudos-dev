var numero = 8;

switch (numero) {
  case 1:
    console.log(`Você digitou o número UM.`);
    break;
  case 2:
    console.log(`Você digitou o número DOIS.`);
    break;
  case 3:
    console.log(`Você digitou o número TRÊS.`);
    break;
  case 4:
    console.log(`Você digitou o número QUATRO.`);
    break;
  case 5:
    console.log(`Você digitou o número CINCO.`);
    break;
  case 6:
    console.log(`Você digitou o número SEIS.`);
    break;
  default:
    console.log(`Número Invalido.`);
    break;
}
